// Пакет logger предоставляет реализацию системы логирования на основе zerolog.
//
// Основные компоненты:
//
// 1. ZerologLogger - основная реализация логгера, использующая zerolog:
//   - Поддерживает все уровни логирования (Info, Error, Debug, Warn, Fatal)
//   - Включает форматированный вывод (Infof, Errorf и т.д.)
//   - Поддерживает логирование со стектрейсом
//   - Позволяет добавлять контекстные поля
//
// 2. ZerologEvent - обертка над событием zerolog:
//   - Инкапсулирует работу с событиями zerolog
//   - Предоставляет методы для логирования с контекстом
//
// Пакет реализует все интерфейсы из logger/interfaces.
//
// Пример использования:
//
//	logger := logger.NewZerologLogger(os.Stdout)
//
//	// Простое логирование
//	logger.Info("Сервер запущен")
//
//	// Логирование с форматированием
//	logger.Infof("Порт: %d", 8080)
//
//	// Логирование ошибок со стектрейсом
//	if err != nil {
//	    logger.ErrorWithStack(err, "Ошибка при обработке запроса")
//	}
//
//	// Логирование с дополнительными полями
//	logger.WithFields(map[string]interface{}{
//	    "user_id": 123,
//	    "action": "login",
//	}).Info("Пользователь вошел в систему")
package logger
